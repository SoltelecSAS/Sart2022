    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        Connection conexion = null;
        List<Integer> listaDefectos = new ArrayList<>();

        if (jCheck10119.isSelected()) {
            listaDefectos.add(10119);
        }
        
        if (ensePedal.isSelected()) {
            listaDefectos.add(10133);
        }
        if (jCheckBox10115.isSelected()) {
            listaDefectos.add(10115);
        }
        if (jCheckBox110116.isSelected()) {
            listaDefectos.add(10116);
        }
        if (jCheckBox10117.isSelected()) {
            listaDefectos.add(10117);
        }
        if (jCheckBox10120.isSelected()) {
            listaDefectos.add(10120);
        }
        if (checkNuevo.isSelected()) {
            listaDefectos.add(10118);
        }

        try {
            conexion = DBUtil.getConnection();
            conexion.setAutoCommit(false);

            String statement = "INSERT INTO db_cda.defxprueba(id_defecto,id_prueba) VALUES(?,?)";
            //JOptionPane.showMessageDialog(null, "IdPrueba = " +idPrueba);
            PreparedStatement instruccion = (PreparedStatement) conexion.prepareStatement(statement);
            for (Integer defecto : listaDefectos) {
                instruccion.setInt(1, defecto);
                instruccion.setLong(2, idPrueba);
                //instruccion.setInt(3, hojaPruebasActual);
                //instruccion.setString(4, "A");
                instruccion.executeUpdate();
                instruccion.clearParameters();
            }
            conexion.commit();
            conexion.setAutoCommit(true);
            conexion.close();

        } catch (ClassNotFoundException | SQLException ex) {
            Mensajes.mostrarExcepcion(ex);
        } finally {
            this.setVisible(false);
            this.dispose();
        }
    }//GEN-LAST:event_guardarActionPerformed

